     <p><b>The Health and Safety Executive Ireland recommends the following measures to protect against COVID-19:</b></p>
     <ul>
        <li>Frequently wash your hands with soap and water for at least 20 seconds.</li>
        <li>Wear a face covering in crowded indoor settings where it is not possible to maintain 2m physical distance from others.</li>
        <li>Maintain at least 2m physical distance from others in both indoor and outdoor settings.</li>
        <li>Avoid touching your face, especially your eyes, nose, and mouth.</li>
     </ul>
        <p><b>If you have symptoms such as:</b></p>
        <ul>
            <li>A cough</li>
            <li>Fever</li>
            <li>Loss of taste or smell</li>
        </ul>
        <p><b>It is advised to:</b></p>
        <ul>
            <li>Self-isolate immediately and arrange a COVID-19 test.</li>
            <li>If the test is positive, continue to self-isolate for at least 10 days from onset of symptoms and until you have been symptom-free for at least 24 hours.</li>
            <li>Close contacts of the positive case should also self-isolate for 14 days.</li>
        </ul>
        <p><b>It is important to:</b></p>
        <ul>
            <li>Follow public health advice and keep updated on local COVID-19 situation.</li>
            <li>Comply with any restrictions or guidelines in your area, including those related to travel, gatherings, and business operations.</li>
            <li>Take steps to protect your mental health and well-being during this time.</li>
        </ul>
        <p>By taking these precautions and following public health advice, we can all work together to reduce the spread of COVID-19 and protect ourselves, our families, and our communities.</p>

